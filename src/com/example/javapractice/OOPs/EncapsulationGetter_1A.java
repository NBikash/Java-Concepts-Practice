package com.example.javapractice.OOPs;

public class EncapsulationGetter_1A {
	private int qty;
	private String name;
	EncapsulationGetter_1A(int qty,String name){
		this.qty=qty;
		this.name=name;
	}
	public int getQty() {
		return qty;
	}
	public String getName() {
		return name;
	}
	 
}
